// string de aspas duplas
let nome = "Juan";
console.log(nome);

//  string usando aspas simples
let sobrenome = 'Urrutia';
console.log(sobrenome);

// inteiro
let idade = 22;
console.log(idade);

// decimal
let altura = 1.75;
console.log(altura);

// booleano true
let diaEstaChuvoso = true;
console.log(diaEstaChuvoso);

// booleano false
let vouPraPraia = false;
console.log(vouPraPraia);

// valor Not A Number ou NaN
let naoNumerico = NaN;
console.log(naoNumerico);

// valor Null (Nulo)
let jogadores = null;
console.log(jogadores);

// valor undefined
let espectadores = undefined;
espectadores = 10;
console.log(espectadores);

// objeto literal
let moto = {
    marca: "Honda",
    ano: "2019",
    modelo: "CB500"
}
console.log(moto);

// array
let series = ["The 100","Z Nation","13 Reasons why"];
console.log(series);

